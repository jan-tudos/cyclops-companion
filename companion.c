// #define __STDC_FORMAT_MACROS 1
#include <inttypes.h>
#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <time.h>

#include <libusb-1.0/libusb.h>


struct action {
	char msg[100];
	unsigned char bytes[60];
};

static
char const *
get_file_name(char const * const path)
{
	char const * const sep = strrchr(path, '/');

	// ignore separator if any
	return (sep ? sep + 1 : path);
}


int
main(int const argc, char const * const * const argv)
{
	// device info
	uint16_t const vend_id = 0x6e30;
	uint16_t const prod_id = 0xfef4;
	int const interface_num = 0;  // video control interface


	// control transfer parameters
	uint8_t  const bmRequestType = 0x21;   // Direction = Host to Device | Type = Class | Recipient = Interface
	uint8_t  const bRequest      = 0x01;   // SET_CUR
	uint16_t const wValue        = 0x0200; // (control selector 0x02) << 8 | 0x00
	uint16_t const wIndex        = 0x0200; // (entity ID 0x02) << 8 | 0x00


	// sanity check
	if (argc != 1) {
		fprintf(stderr, "No parameters, please.\n");
		return EXIT_FAILURE;
	}


	// Who are we?
	char const * const name = get_file_name(argv[0]);

	struct action acts[4]; // up to 4 is enough for us; KISS!
	size_t num_acts = 0;   // how many labours are there for us to perform?

	// No need to leak the magic payloads into function scope...
	{
		unsigned char wake[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x35, 0x2c, 0xf1, 0x0, 0xe1, 0x13, 0xc2, 0x1, 0x1, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char unlock[] = {0xaa, 0x0, 0xc, 0x10, 0x0, 0xc4, 0xcb, 0xed, 0x0, 0xe3, 0x30, 0x66, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char no_follow[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x28, 0xb9, 0x77, 0x0, 0xe3, 0x30, 0x90, 0x0, 0x0, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char no_zoom[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x2b, 0xd9, 0x62, 0x0, 0xe3, 0x30, 0x90, 0x1, 0x0, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char follow[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x7, 0x48, 0x6e, 0x0, 0xe3, 0x30, 0x90, 0x0, 0x1, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char zoom[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x2c, 0x29, 0x85, 0x0, 0xe3, 0x30, 0x90, 0x1, 0x1, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};
		unsigned char suspend[] = {0xaa, 0x0, 0xe, 0x10, 0x0, 0x34, 0x7d, 0x7d, 0x0, 0xe1, 0x13, 0xc2, 0x1, 0x3, 0x3e, 0x1, 0x2, 0x1, 0x0, 0x0, 0x0, 0x14, 0x0, 0x1, 0x1, 0x0, 0x0, 0x0, 0x1, 0x0, 0x0, 0x0, 0x0, 0x40, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0, 0x0};

		// What are we supposed to do? Depends on who we are...
		if (0 == strcmp(name, "siren")) {
			strcpy(acts[0].msg, "Wake up camera");
			memcpy(acts[0].bytes, wake, sizeof wake);
			strcpy(acts[1].msg, "Unlock from any target the camera has");
			memcpy(acts[1].bytes, unlock, sizeof unlock);
			strcpy(acts[2].msg, "Disable 'Follow' gesture");
			memcpy(acts[2].bytes, no_follow, sizeof no_follow);
			strcpy(acts[3].msg, "Disable 'Zoom' gesture");
			memcpy(acts[3].bytes, no_zoom, sizeof no_zoom);
			num_acts = 4;
		}
		else if (0 == strcmp(name, "panacea")) {
			strcpy(acts[0].msg, "Wake up camera");
			memcpy(acts[0].bytes, wake, sizeof wake);
			strcpy(acts[1].msg, "Enable 'Follow' gesture");
			memcpy(acts[1].bytes, follow, sizeof follow);
			strcpy(acts[2].msg, "Enable 'Zoom' gesture");
			memcpy(acts[2].bytes, zoom, sizeof zoom);
			num_acts = 3;
		}
		else if (0 == strcmp(name, "morpheus")) {
			strcpy(acts[0].msg, "Suspend camera");
			memcpy(acts[0].bytes, suspend, sizeof suspend);
			num_acts = 1;
		}
		else {
			fprintf(stderr, "Unknown binary name '%s'. That's not me.\n", argv[0]);
			return EXIT_FAILURE;
		}
	}

	// init libusb
	int ret = libusb_init_context(/*ctx=*/NULL, /*options=*/NULL, /*num_options=*/0);
	if (LIBUSB_SUCCESS != ret) {
		fprintf(stderr, "libusb_init failed: %s (%d)\n", libusb_strerror(ret), ret);
		return EXIT_FAILURE;
	}
	fprintf(stderr, "libusb init done\n");


	// find correct device
	struct libusb_device **devs;
	struct libusb_device *dev;

	if (libusb_get_device_list(/* ctx=*/ NULL, &devs) <= 0) {
		fprintf(stderr, "Failed to enumerate USB devices: %s (%d)\n", libusb_strerror(ret), ret);
		goto fail_init;
	}

	size_t cur_dev = 0;
	while ( NULL != (dev = devs[cur_dev++])) {
		struct libusb_device_descriptor desc;
		ret = libusb_get_device_descriptor(dev, &desc);
		if (LIBUSB_SUCCESS != ret) {
			fprintf(stderr, "Failed to get device descriptor: %s (%d)\n", libusb_strerror(ret), ret);
			goto fail_list;
		}

		if (desc.idVendor == vend_id && desc.idProduct == prod_id) {
			break;
		}
	}

	if (!dev) {
		fprintf(stderr, "Camera not found.\n");
		goto fail_list;
	}
	fprintf(stderr, "Camera found.\n");


	// open device
	libusb_device_handle * devh;
	ret = libusb_open(dev, &devh);
	if (LIBUSB_SUCCESS != ret) {
		fprintf(stderr, "Failed to open device: %s (%d)\n", libusb_strerror(ret), ret);
		fprintf(stderr, "Do you have *write* access on the USB device "
		 "(/dev/bus/usb/%03" PRIu8 "/%03" PRIu8 ")?\n",
		 libusb_get_bus_number(dev),
		 libusb_get_device_address(dev)
		);
		goto fail_list;
	}


	// enable automatic detaching and re-attaching of kernel driver
	ret = libusb_set_auto_detach_kernel_driver(/*dev_handle=*/devh, /*enable=*/1);
	if (LIBUSB_SUCCESS != ret) {
		fprintf(stderr, "Failed to setup auto detaching of kernel driver: %s (%d)\n", libusb_strerror(ret), ret);
		goto fail_dev;
	}


	// claim interface (required before communication)
	ret = libusb_claim_interface(/*dev_handle=*/devh, interface_num);
	if (LIBUSB_SUCCESS != ret) {
		fprintf(stderr, "Failed to claim interface: %s (%d)\n", libusb_strerror(ret), ret);
		goto fail_dev;
	}
	fprintf(stderr, "Device claimed.\n");


	// send control transfer packet
	for (size_t cc = 0; cc < num_acts; ++cc) {
		uint16_t const wLength = sizeof acts[cc].bytes; // payload length
		do {
			ret = libusb_control_transfer(
				/*dev_handle=*/ devh,
				bmRequestType,
				bRequest,
				wValue,
				wIndex,
				acts[cc].bytes,
				wLength,
				/*timeout=*/ 1300 /*ms*/ // arbitrary, nice number
			);
			fprintf(stderr, "%s... (%d bytes)\n", acts[cc].msg, ret);
			if (ret < 0) {
				fprintf(stderr, "libusb_control_transfer failed: %s (%d)\n", libusb_strerror(ret), ret);
				goto fail_dev;
			}
		} while (ret < wLength); // retry on partial transfer

		// give the camera a brief rest of 42ms (no error check...)
		struct timespec sleep_dur = {.tv_nsec = 42000000};
		nanosleep(&sleep_dur, /* rem= */ NULL);
	}
	fprintf(stderr, "All commands complete\n");


	// cleanup
	libusb_release_interface(/*dev_handle=*/devh, interface_num);

	// explicitly re-attach kernel driver
	// (should've already been done automatically but sometimes seems to fail)
	libusb_attach_kernel_driver(devh, interface_num);
	libusb_close(/*dev_handle=*/devh);
	libusb_free_device_list(devs, /*unref_devices=*/ 1);
	libusb_exit(/*ctx=*/NULL);
	fprintf(stderr, "Cleanup done\n");
	return EXIT_SUCCESS;


fail_dev:
	libusb_close(/*dev_handle=*/devh);
fail_list:
	libusb_free_device_list(devs, /*unref_devices=*/ 1);
fail_init:
	libusb_exit(/*ctx=*/NULL);

	return EXIT_FAILURE;
}
